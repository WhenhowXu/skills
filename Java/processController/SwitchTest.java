public class SwitchTest {
    public static void main(String[] args){
        int option = 1;
        switch(option){
            case 1:
                System.out.println("苹果");
                break;
            case 2:
                System.out.println("香蕉");
                break; 
            default:
                System.out.println("橘子");
                break;
        }
    }
}
